package com.lovo.forestPrevention.bean.dataBean1;


public class SysSick {
  //病害id
  private long sickId;
  //病害名称
  private String sickName;
  //病原
  private String sickSource;
  //发病症状
  private String sickSymptom;
  //发病规律
  private String sickRule;
  //图片
  private String sickImgPath;
  //防治措施
  private String sickMethod;
  //主要危害
  private String sickHarm;


  public long getSickId() {
    return sickId;
  }

  public void setSickId(long sickId) {
    this.sickId = sickId;
  }


  public String getSickName() {
    return sickName;
  }

  public void setSickName(String sickName) {
    this.sickName = sickName;
  }


  public String getSickSource() {
    return sickSource;
  }

  public void setSickSource(String sickSource) {
    this.sickSource = sickSource;
  }


  public String getSickSymptom() {
    return sickSymptom;
  }

  public void setSickSymptom(String sickSymptom) {
    this.sickSymptom = sickSymptom;
  }


  public String getSickRule() {
    return sickRule;
  }

  public void setSickRule(String sickRule) {
    this.sickRule = sickRule;
  }


  public String getSickImgPath() {
    return sickImgPath;
  }

  public void setSickImgPath(String sickImgPath) {
    this.sickImgPath = sickImgPath;
  }


  public String getSickMethod() {
    return sickMethod;
  }

  public void setSickMethod(String sickMethod) {
    this.sickMethod = sickMethod;
  }


  public String getSickHarm() {
    return sickHarm;
  }

  public void setSickHarm(String sickHarm) {
    this.sickHarm = sickHarm;
  }

}
